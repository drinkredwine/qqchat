<script setup>
import { ref } from 'vue';

const messageText = ref('');
const emit = defineEmits(['send-message']);

const sendMessage = () => {
  if (messageText.value.trim()) {
    emit('send-message', messageText.value);
    messageText.value = '';
  }
};
</script>